

CREATE PROCEDURE DIM_LOCATION_INSERT(
    _COUNTRY VARCHAR(100),
    _TIMEZONE INT,
    _ID INT,
    _LOC_NAME VARCHAR(100),
    _LON NUMERIC,
    _LAT NUMERIC,
    _API_ID NUMERIC,
	_SYS_INSERT_DATETIME TIMESTAMP
)
LANGUAGE plpgsql
AS $$
BEGIN
	INSERT INTO LOCATION (
		COUNTRY,
		TIMEZONE,
		ID,
		LOC_NAME,
		LON,
		LAT,
	    API_ID,
		SYS_INSERT_DATETIME
)
VALUES (
	_COUNTRY
	,_TIMEZONE
	,_ID
	,_LOC_NAME
	,_LON
	,_LAT
	,'-1'
	,'2024-03-16 23:09:32.699453'
);
END
$$;


